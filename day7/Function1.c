#include <stdio.h>

// 함수 바깥에서 만드는 변수는 '전역변수', 한번 만들면 없어지지 않으나
// 많이 만들면 프로그램 성능이 저하됨 (좁은 공간에 생성되어 삭제되지 않고 계속 차지하고 있음)
